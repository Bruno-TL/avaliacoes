let numero = 1;

if(numero % 2 === 0) {
    document.write(`O ${numero} é par`)
    console.log(`O ${numero} é par`)
}else {
    document.write(`O ${numero} é ímpar`)
    console.log(`O ${numero} é ímpar`)
}